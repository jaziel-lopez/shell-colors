#! /bin/bash

#
# This unsets all of the variables in colors.sh
#
# To use it, source it like this:
#
#   . /path/to/colors_unset.sh
#


# Colors

# Reset
unset Color_Off
 
# Regular Colors
unset Black
unset Red
unset Green
unset Yellow
unset Blue
unset Purple
unset Cyan
unset White
 
# Bold
unset BBlack
unset BRed
unset BGreen
unset BYellow
unset BBlue
unset BPurple
unset BCyan
unset BWhite
 
# Underline
unset UBlack
unset URed
unset UGreen
unset UYellow
unset UBlue
unset UPurple
unset UCyan
unset UWhite
 
# Background
unset On_Black
unset On_Red
unset On_Green
unset On_Yellow
unset On_Blue
unset On_Purple
unset On_Cyan
unset On_White
 
# High Intensity
unset IBlack
unset IRed
unset IGreen
unset IYellow
unset IBlue
unset IPurple
unset ICyan
unset IWhite
 
# Bold High Intensity
unset BIBlack
unset BIRed
unset BIGreen
unset BIYellow
unset BIBlue
unset BIPurple
unset BICyan
unset BIWhite
 
# High Intensity backgrounds
unset On_IBlack
unset On_IRed
unset On_IGreen
unset On_IYellow
unset On_IBlue
unset On_IPurple
unset On_ICyan
unset On_IWhite
